package Bonus;

public class Product 

{
    public int ID;
    public String Name;
    public double Price;
    
    
    
	public Product() 
	{
		super();
	}


	public Product(int iD, String name, double price) 
	{
		super();
		ID = iD;
		Name = name;
		Price = price;
	}
	
	
    
    
    
    
}
